namespace Lumina.Forms;

/// <summary>
/// Specifies whether a control is arranged vertically or horizontally.
/// </summary>
public enum Orientation
{
    /// <summary>
    /// Arranges content left-to-right with a vertical divider.
    /// </summary>
    Vertical = 0,

    /// <summary>
    /// Arranges content top-to-bottom with a horizontal divider.
    /// </summary>
    Horizontal = 1,
}
