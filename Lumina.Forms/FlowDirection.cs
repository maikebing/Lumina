namespace Lumina.Forms;

/// <summary>
/// Specifies how controls are laid out in a flow layout container.
/// </summary>
public enum FlowDirection
{
    /// <summary>
    /// Places controls from left to right.
    /// </summary>
    LeftToRight = 0,

    /// <summary>
    /// Places controls from top to bottom.
    /// </summary>
    TopDown = 1,

    /// <summary>
    /// Places controls from right to left.
    /// </summary>
    RightToLeft = 2,

    /// <summary>
    /// Places controls from bottom to top.
    /// </summary>
    BottomUp = 3,
}
